<h4 class="modal-title">FRAUD CASE</h4>
</div>
<div class="modal-body">
<ul>
  <li>Fraud is one of the categories of the inbound calls received by the call center agent. A case is suspected to be fraud base on the incidence reported by the customer.</li>
<li>A suspected fraud case is meant to be escalated to the appropriate department in Bank of industry for further investigation.</li>
<li>For Fraud cases, there are various types of such cases hence there are also details needed before such a case can be reported as fraud case.</li>
<li>Some of the suspected fraud cases include;
  <ol>
    <li>Agent denying customers access to the loan.</li>
  <li>Agent not giving full value of loan to the customer or not releasing the loan at all to the customer.</li>
  <li>Agent trading with customer’s loan on behalf of the customer.</li>
  <li>Agent not remitting customer loan repayment to Bank of Industry.</li>
</ol>
Therefore with such examples, it is necessary to get all facts concerning every cases to be reported.</li>
<li>Some of the required information to be captured by the call center agent include:
Customer’s name, Customer’s phone number, Association of the customer, Agent name, Agent phone number and Action (Action is the comment suggested by the call centers agent to be taking by Bank of Industry on the suspected cases).</li>
</ul>
